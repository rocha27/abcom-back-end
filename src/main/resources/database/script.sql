CREATE TABLE SGCAM_MAPA.S_CAMARA(
	ID_CAMARA NUMBER(10) NOT NULL,
	NM_NOME VARCHAR(256) NOT NULL,
	NM_NOMECLATURA VARCHAR(100) NOT NULL,
	CD_TIPO_CAMARA NUMBER(1) NOT NULL CONSTRAINT CK_CAMARA_CD_TIPO_CAMARA CHECK( CD_TIPO_CAMARA IN(1,2) ),
	CD_TIPO_SITUACAO_CAMARA NUMBER(1) NOT NULL CONSTRAINT CK_CAMARA_CD_TP_SITU_CAMARA CHECK( CD_TIPO_SITUACAO_CAMARA IN(1,2) ),
	DH_CRIACAO_REGISTRO TIMESTAMP NOT NULL,
	ID_REPRESENT_SECRETARIO NUMBER(10) NOT NULL,
	ID_REPRESENT_SUPERVISOR NUMBER(10),
	ID_REPRESENT_CONSULTOR_TECNICO NUMBER(10),
	ID_REPRESENT_PRESIDENTE NUMBER(10),

	CONSTRAINT PK_CAMARA PRIMARY KEY (ID_CAMARA),
	CONSTRAINT FK_CAMARA_REPRESENT_SECRETARIO FOREIGN KEY (ID_REPRESENT_SECRETARIO) REFERENCES SGCAM_MAPA.S_REPRESENTANTE(ID_REPRESENTANTE),
	CONSTRAINT FK_CAMARA_REPRESENT_SUPERVISOR FOREIGN KEY (ID_REPRESENT_SUPERVISOR) REFERENCES SGCAM_MAPA.S_REPRESENTANTE(ID_REPRESENTANTE),
	CONSTRAINT FK_CAMARA_REPRESENT_CONSU_TEC FOREIGN KEY (ID_REPRESENT_CONSULTOR_TECNICO) REFERENCES SGCAM_MAPA.S_REPRESENTANTE(ID_REPRESENTANTE),
	CONSTRAINT FK_CAMARA_REPRESENT_PRESIDENTE FOREIGN KEY (ID_REPRESENT_PRESIDENTE) REFERENCES SGCAM_MAPA.S_REPRESENTANTE(ID_REPRESENTANTE)
);
