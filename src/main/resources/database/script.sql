CREATE TABLE SGCAM_MAPA.S_REPRESENTANTE(
	ID_REPRESENTANTE NUMBER(10) NOT NULL,
	NM_NOME VARCHAR(250) NOT NULL,
	DS_EMAIL VARCHAR(250),
	NR_CPF CHAR(11) NOT NULL UNIQUE,
